	<p>We control 8 of the 11 links on the first results page on Google for [<a href="http://www.google.com/search?q=levees.org">levees.org</a>].  How?  Syndication is key:</p>
	<ol>
		<li>Syndicate content at all major video sites using <a href="http://tubemogul.com" target="_blank">TubeMogul</a>.</li>
		<li>Syndicate Twitter and Blog feeds in as many places as possible, such as Facebook, Myspace, Tumblr, Friendfeed, LinkedIn, Plaxo and more.</li>
		<li>Cross post on other blogs.  We do this on <a href="http://blog.nola.com/levees" target="_blank">NOLA.com</a>.  (Bonus: these posts sometimes appear on Google as "news.")</li>
		<li>Pay <a href="http://prweb.com" target="_blank">PRweb</a> to publish press releases on various sites.  (<a href="http://www.google.com/search?q=%22Levees.Org+Expands+National+Presence+by+Establishing+Its+Sixth+Chapter%3A+New+York%22&amp;aq=f&amp;oq=&amp;aqi=" target="_blank">example</a>)</li>
	</ol>

	<h3>Dominate YouTube</h3>
	<p>We used to host our own videos using a paid service.  Big mistake.  Now we host all of our videos for free on <a href="http://youtube.com/leveesorg" target="_blank">YouTube</a>, where they can go viral.  Consequently, YouTube is our #4 top website referrer, sending mostly new visitors.</p>
	<p>Our primary goal is always to be featured on the YouTube top-100 lists.  Here's how we get there:</p>
	<ol>
		<li>We're never shy about asking our members to login or create accounts at YouTube to rate, "favorite," and comment on our video.  We even use YouTube annotations over our video to point to the rate button.  As a result, our videos are often featured as the highest rated or most commented-on videos.</li>
		<li>Whenever we upload a video, we immediately do a massive push to drive as much traffic as possible.  We give our members a 24-hour timeframe to watch the video and send to their friends, and we even coordinate these efforts in advance with bloggers.  As a result, we make it to the top-100 "viewed today" page more often than an organization our size should.</li>
	</ol>
