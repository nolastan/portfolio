	<h3>Leverage Wikipedia</h3>
	<p>Most people don't think of Wikipedia in their social media marketing efforts.  They should.</p>
	<p>Wikipedia is our #2 top traffic source and #1 referrer; nearly 5% of our traffic comes from Wikipedia.  This is because our members have placed <span>Levees</span>.<span>Org</span> links under the external links section of relevant Wikipedia articles.  Along with adding links to articles, we have spent time improving articles and interlinking related articles.</p>
	<p>We've also created entirely new articles, such as <a href="http://en.wikipedia.org/wiki/Flood_Control_Act_of_1965" target="_blank">Flood Control Act of 1965</a>.  It didn't take much effort; the article started like <a href="http://en.wikipedia.org/w/index.php?title=Flood_Control_Act_of_1965&amp;oldid=126647604" target="_blank">this</a> and was expanded by the community.  In fact, we didn't even add our link to that article; someone else did later on.</p>
