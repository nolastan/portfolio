	<h1>Social Media for Non-Profit</h1>
	<h2>By <a href="http://stanfordrosenthal.com">Stanford Rosenthal</a> on November 18th, 2009</h2>

	<h3>Background</h3>
	<p><a href="http://levees.org">Levees.Org</a> is a not-for-profit grassroots organization that my mother and I founded in December of 2005 while we were exiled from New Orleans.  Our mission is education that the catastrophic flooding of New Orleans was a <a href="http://www.upi.com/Top_News/2006/04/06/Corps-of-Engineers-admits-design-failure/UPI-91851144355032/">man-made federal disaster</a>, not a natural one.  Today we have over 24,000 members and chapters in six states.</p>
	<p>I have decided to share what we have learned in order to help other organizations and causes.  It's important to note that none of this requires a social media professional.  Once everything is setup, the workflow is simple.  (If you need proof, ask my mother!  She's been running our online presence for months now without any assistance from me.)</p>
