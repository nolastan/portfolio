	<h3>Rethink Conversion Goals</h3>
	<p>In 2005, all that mattered to us as far as social media was collecting e-mail addresses.  We did a great job at this thanks to the help of Internet Marketing expert Ken McCarthy.  However, eventually we realized (as Ken McCarthy <a href="http://kenmccarthy.com/blog/2009/11/05/skeptic-surrender-twitter-wins/" target="_blank">finally</a> has <a href="http://twitter.com/kenmccarthy" target="_blank">as well</a>) that people aren't using e-mail as their primary form of communication; many young people don't seem to be using it at all.  We now think of gaining Facebook and Twitter followers as important as collecting e-mail addresses.</p>
