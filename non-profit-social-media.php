	<h3>Conquer Google</h3>
	<figure class="right">
		<a href="http://google.com/search?q=levees.org" target="_blank"><img title="Levees.Org on Google" src="http://stanfordrosenthal.com/wp-content/uploads/2009/11/Screen-shot-2009-11-18-at-12.32.23-PM.png" alt="Google search results for Levees.Org" width="278" height="41" /></a>
